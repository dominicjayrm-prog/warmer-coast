// Generates SQL to seed wc_modules / wc_module_sections / wc_module_checklist
// from the playbook content files in src/lib/playbook-content/.
//
// Run:   node scripts/generate-modules-seed.mjs > scripts/modules-seed.sql
// Apply: Supabase SQL editor or psql. Idempotent (delete-by-country/slug first).
//
// The content files are TypeScript but deliberately follow a constrained
// shape (two typed helper consts + one typed exported array) so this script
// can strip the annotations and evaluate them without a TS toolchain.

import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

const sqlEscape = (s) => "'" + String(s).replace(/'/g, "''") + "'";
const sqlNull = (v) => (v == null || v === '' ? 'null' : sqlEscape(v));

function loadModules(file, exportName) {
  let src = readFileSync(resolve(file), 'utf8');
  src = src
    // drop type-only imports
    .replace(/^import type .*$/gm, '')
    // strip the helper signatures' annotations (exact shapes we control)
    .replace(
      "(tone: 'deadline' | 'warning' | 'tip' | 'money', title: string, body: string)",
      '(tone, title, body)',
    )
    .replace('(headers: string[], rows: string[][])', '(headers, rows)')
    .replace(/: Record<string, string>/g, '')
    // exported array annotation
    .replace(/export const (\w+): PlaybookModule\[\] =/, 'const $1 =');
  // eslint-disable-next-line no-new-func
  return new Function(`${src}; return ${exportName};`)();
}

const data = {
  spain: loadModules('src/lib/playbook-content/spain.ts', 'spainModules'),
  portugal: loadModules('src/lib/playbook-content/portugal.ts', 'portugalModules'),
  gibraltar: loadModules('src/lib/playbook-content/gibraltar.ts', 'gibraltarModules'),
};

const lines = [];
lines.push('-- Seed WarmerCoast modules from src/lib/playbook-content/*.ts');
lines.push('-- Generated by scripts/generate-modules-seed.mjs — do not hand-edit.');
lines.push('-- Idempotent: deletes by (country, slug) first; sections/checklist cascade or are re-inserted.');
lines.push('');

for (const [country, modules] of Object.entries(data)) {
  for (const mod of modules) {
    lines.push(`-- ${country}/${mod.slug}`);
    lines.push(
      `delete from public.wc_module_sections where module_id in (select id from public.wc_modules where country = ${sqlEscape(country)} and slug = ${sqlEscape(mod.slug)});`,
    );
    lines.push(
      `delete from public.wc_module_checklist where module_id in (select id from public.wc_modules where country = ${sqlEscape(country)} and slug = ${sqlEscape(mod.slug)});`,
    );
    lines.push(
      `delete from public.wc_modules where country = ${sqlEscape(country)} and slug = ${sqlEscape(mod.slug)};`,
    );

    const sectionSelects = mod.sections.map(
      // Bodies are already full HTML — insert verbatim, no <p> wrapping.
      (s, i) => `select id, ${i}, ${sqlEscape(s.title)}, ${sqlEscape(s.body)} from m`,
    );
    const checklistSelects = mod.checklist.map(
      (c, i) =>
        `select id, ${i}, ${sqlEscape(c.id)}, ${sqlEscape(c.label)}, ${sqlNull(c.detail)}, ${sqlNull(c.deadline)} from m`,
    );

    const cte = `with m as (insert into public.wc_modules (country, slug, module_number, title, summary, duration, published) values (${sqlEscape(country)}, ${sqlEscape(mod.slug)}, ${mod.n}, ${sqlEscape(mod.title)}, ${sqlEscape(mod.summary)}, ${sqlEscape(mod.duration)}, true) returning id)`;

    if (sectionSelects.length > 0 && checklistSelects.length > 0) {
      lines.push(
        `${cte}, s as (insert into public.wc_module_sections (module_id, position, title, body_html) ${sectionSelects.join(' union all ')} returning module_id) insert into public.wc_module_checklist (module_id, position, item_id, label, detail, deadline) ${checklistSelects.join(' union all ')};`,
      );
    } else if (sectionSelects.length > 0) {
      lines.push(
        `${cte} insert into public.wc_module_sections (module_id, position, title, body_html) ${sectionSelects.join(' union all ')};`,
      );
    } else {
      lines.push(`${cte} select id from m;`);
    }
    lines.push('');
  }
}

console.log(lines.join('\n'));
